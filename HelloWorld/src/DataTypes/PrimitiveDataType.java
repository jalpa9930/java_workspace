package DataTypes;

public class PrimitiveDataType {

	public static void main(String[] args) {

		byte b= 40;
		short s =200;
		int i = 1000;
		long l = 123456789; // in long only 9 characters allowed
		
		double d = 12.33;
		float e = 46.66f; // in float f is compulsory after value
		
		char c = 'a';
		boolean a = true;
		
		b= 100; // if you change the value then new value consider
		
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		System.out.println(d);
		System.out.println(e);
		System.out.println(c);
		System.out.println(a);

		
		
}
	
}
